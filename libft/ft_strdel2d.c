#include "libft.h"

void	ft_strdel2d(char **s)
{
	int		i;

	i = 0;
	if (s != NULL)
	{
		while (s[i])
		{
			ft_strdel(&s[i]);
			i++;
		}
		free(s);
		s = NULL;
	}
}
