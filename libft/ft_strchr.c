char	*ft_strchr(const char *s, int c)
{
	char	a;
	int		i;
	char	*p;

	i = 0;
	a = (char)c;
	while (s[i] != '\0')
	{
		if (s[i] == a)
		{
			p = &((char *)s)[i];
			return (p);
		}
		i++;
	}
	if (a == '\0')
	{
		p = &((char *)s)[i];
		return (p);
	}
	return (0);
}
